     public void showdata(String date, String local)
    {
        
        String FilegameDate;
        String FilePlayerName1;
        String FilePlayerScore1;
        String FilePlayerName2;
        String FilePlayerScore2;
        System.out.println("data\n" +date);
        int btn1;
        String value;
        LinkedHashMap<Integer, String> moves = new LinkedHashMap<>();
        String[] splitline = local.split("/n");
        
        for(int x = 0; x<splitline.length; x++)
        {
            
            String [] line = splitline[x].split(",");
            FilegameDate = line[0];
            System.out.println("data\n" +date);
            if(FilegameDate.equalsIgnoreCase(date))
            {
                System.out.println("inside FilegameDate\n" +FilegameDate);
                FilePlayerName1 = line[1];
                FilePlayerScore1 = line[2];
                FilePlayerName2 = line[3];
                FilePlayerScore2 = line[4];
                labelFirstPlayerName.setText(FilePlayerName1);
                labelFirstScore.setText(FilePlayerScore1);
                labelSecondPlayerName.setText(FilePlayerName2);
                labelSecondScore.setText(FilePlayerScore2);    

                
                
                for (int j = 5, k = 6; j < line.length  && k < line.length ; j += 2, k += 2) {
                    btn1 = Integer.parseInt(line[j]);
                    value = line[k];
                    moves.put(btn1, value);
                    System.out.println("aaaaaaaaaaaaaaaaaaaaaaaaaaaa" + btn1 + "/" + value);
                }
                
            
            System.out.println("out side for loop");
            Thread n = new Thread(new Runnable() {
                @Override
                public void run() {
                    
                
                for (int key:moves.keySet())
                {
                    try {
                        System.out.print(key+" string in button::"+moves.get(key)+"\n");
                        //label[key].setFont(new Font("Arial", Font.BOLD, 0));
                        label[key].setText(moves.get(key));
                        if((moves.get(key)).equals("X"))
                            label[key].setIcon(new ImageIcon(getClass().getClassLoader().getResource("images/x.png")));
                        else if((moves.get(key)).equals("O"))
                            label[key].setIcon(new ImageIcon(getClass().getClassLoader().getResource("images/o.png")));
                        
                        
                        Thread.sleep(1000);
                        
                    } catch (InterruptedException ex) {
                        Logger.getLogger(ShowGame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
            
                }
            });
            n.start(); 
            }
        }
        
    }

